#include "libft.h"

void	ft_sort_select(int *arr, int n)
{
	int i;

	i = 1;
	while (i < n)
	{
		if (arr[i - 1] > arr[i])
		{
			ft_swap(&arr[i - 1], &arr[i]);
			i = 0;
		}
		i++;
	}
}
