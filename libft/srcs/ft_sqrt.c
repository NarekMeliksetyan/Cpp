int		ft_sqrt(int nb)
{
	int sq;

	if (nb <= 0)
		return (0);
	if (nb == 1)
		return (1);
	sq = 2;
	while (sq <= nb / sq)
	{
		if (sq * sq == nb)
			return (sq);
		sq++;
	}
	return (0);
}
