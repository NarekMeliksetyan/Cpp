int		ft_factorial(int nb)
{
	int fact;

	fact = 1;
	if (nb < 0 || nb > 12)
		return (0);
	if (nb == 0)
		return (1);
	while (nb != 1)
	{
		fact *= nb;
		nb--;
	}
	return (fact);
}
