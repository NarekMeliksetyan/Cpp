char	*ft_strrev(char *str)
{
	int		i;
	int		len;
	char	c;

	len = 0;
	while (str[len])
		len++;
	len--;
	i = 0;
	while (i < ((len + 1) / 2))
	{
		c = str[i];
		str[i] = str[len - i];
		str[len - i] = c;
		i++;
	}
	return (str);
}
