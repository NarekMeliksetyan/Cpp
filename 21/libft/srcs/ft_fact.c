#include "libft.h"

size_t	ft_fact(int nb)
{
	size_t fact;

	fact = 1;
	if (nb < 0 || nb > 20)
		return (0);
	if (nb == 0)
		return (1);
	while (nb != 1)
	{
		fact *= nb;
		nb--;
	}
	return (fact);
}
