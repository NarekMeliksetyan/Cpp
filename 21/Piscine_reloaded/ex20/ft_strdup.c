#include <stdlib.h>

char	*ft_strdup(char *src)
{
	int		i;
	char	*new;

	new = malloc(sizeof(*src));
	if (!new)
		return (0);
	i = 0;
	while (src[i])
	{
		new[i] = src[i];
		i++;
	}
	new[i] = '\0';
	return (new);
}
