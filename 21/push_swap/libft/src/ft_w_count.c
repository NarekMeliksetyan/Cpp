#include "libft.h"

size_t		ft_w_count(char const *s, char c)
{
	size_t i;
	size_t cnt;

	i = 0;
	cnt = 0;
	while (s[i] != 0)
	{
		if ((i == 0 || s[i - 1] == c) && s[i] != c)
			cnt++;
		i++;
	}
	return (cnt);
}
