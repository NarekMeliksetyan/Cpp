#include "libft.h"

int		ft_delint(int n)
{
	int i;
	int len;
	int del;

	i = 0;
	del = 1;
	len = ft_digitcount(n);
	while (i < len - 1)
	{
		del = del * 10;
		i++;
	}
	return (del);
}
