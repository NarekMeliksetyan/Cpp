#include "libft.h"

void		*ft_memset(void *b, int c, size_t len)
{
	unsigned int	i;
	char			*str;

	str = (char *)b;
	i = 0;
	while (i < len)
	{
		str[i] = (char)c;
		i++;
	}
	return (str);
}
